    <h1 class="title">Bem-vindo ao Hacker News</h1>

    <p>O Hacker News é um pouco diferente de outros sites de comunidade e agradeceríamos se você reservasse um minuto para ler o seguinte, bem como as diretrizes oficiais .</p> 

    <p>HN é um experimento. Como regra, a qualidade de um site de comunidade que se torna popular diminui. Nossa hipótese é que isso não é inevitável - fazendo um esforço consciente para resistir ao declínio, podemos evitar que aconteça.</p> 

    <p>Essencialmente, existem duas regras aqui: não poste ou vote a favor de links ruins e não seja rude ou burro nos tópicos de comentários.</p> 

    <p>Um link de merda é apenas superficialmente interessante. As histórias no HN não precisam ser sobre hackers, porque bons hackers não estão apenas interessados ​​em hackear, mas precisam ser profundamente interessantes.</p> 

    <p>O que significa "profundamente interessante"? Significa coisas que ensinam sobre o mundo. Uma história sobre um roubo, por exemplo, provavelmente não seria profundamente interessante. Mas se esse roubo foi um sinal de alguma tendência subjacente maior, talvez pudesse ser.</p> 

    <p>A pior coisa para postar ou votar é algo que é intensamente, mas superficialmente interessante: fofoca sobre pessoas famosas, fotos ou vídeos engraçados ou fofos, artigos políticos partidários, etc. esse tipo de coisa em um site de notícias, vai deixar de lado as coisas profundamente interessantes, que tendem a ser mais silenciosas.</p> 

    <p>O princípio mais importante em HN, entretanto, é fazer comentários ponderados. Pensativo em ambos os sentidos: civil e substancial.</p> 

    <p>O teste de substância é muito parecido com o de links. Seu comentário nos ensina alguma coisa? Existem duas maneiras de fazer isso: apontando algumas considerações que não foram mencionadas anteriormente e dando mais informações sobre o assunto, talvez por experiência pessoal. Enquanto comentários como "LOL!" ou pior ainda, "Isso é retardado!" não nos ensine nada.</p> 

    <p>Comentários vazios podem ser aceitáveis ​​se forem positivos. Não há nada de errado em enviar um comentário dizendo apenas "Obrigado". O que desencorajamos especialmente são os comentários vazios e negativos - comentários que são apenas xingamentos.</p> 

    <p>O que nos leva ao princípio mais importante em NA: civilidade. Muito antes da web, o anonimato das conversas online atraiu as pessoas a serem muito mais rudes do que seriam pessoalmente. Portanto, o princípio aqui é: não diga nada que você não diria cara a cara. Isso não significa que você não pode discordar. Mas discorde sem xingar. Se você estiver certo, seu argumento será mais convincente sem eles.</p> 
